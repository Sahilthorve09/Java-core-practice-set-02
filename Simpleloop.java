
public class Simpleloop {
	public static void main(String[] args) {
		int i = 1;
		int j = 1;
		
		 System.out.println("While Loop:");
		 
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
	    System.out.println("Do While Loop:");
		do{
			System.out.println(j);
			j++;
		}while(j<=10);
	}
}
